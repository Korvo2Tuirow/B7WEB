let frutas = ["Maça", "Banana", "Laranja", "Uva"];

frutas.push("Kiwi"); // adiciona na ultima posição do array

console.log(frutas);

frutas.unshift("Melancia");// adiciona na primeira posição do array

console.log(frutas);

frutas.pop(); // deleta o ultimo array

console.log(frutas);

frutas.shift(); // deleta o primeiro array

console.log(frutas);

frutas[0] = "Pera" // trocou o index 0 de maça para pera

console.log(frutas);

frutas [frutas.length -1] = "Limão"; // troca o ultimo array ou usa o pop e depois o push

console.log(frutas);

frutas.reverse();// inverte as posições dos arrays de trás para frente

console.log(frutas);

frutas.sort();// deixa em ordem alfabética

console.log(frutas);
















